#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*aux;
	int				i;

	i = 0;
	aux = s;
	while (n > 0)
	{
		aux[i] = '\0';
		n--;
		i++;
	}
}
